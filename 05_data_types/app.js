// data types ==> primitive types, reference types

//primitive types ==> string, numbers, boolean, null, undefined, symbol

// reference types ==> object,array


// string
const variable1 = 'jackiieee';
console.log(typeof variable1);

// numbers
const variable2 = 123;
console.log(typeof variable2)
const variable3 = 114.13;
console.log(typeof variable3);

// boolean
const variable4 = true;
console.log(typeof variable4);
const variable5 = false;
console.log(typeof variable5);


// null
const variable6 = null;
console.log(typeof variable6);

// undefined
var variable7;
console.log(typeof variable7);

// symbol
var variable8 = Symbol();
console.log(typeof variable8);

// reference type ==> typeof object
const variable9 = {
    a: 1,
    b: 2
}

console.log(typeof variable9)


const variable10 = [1, 2]

console.log(typeof variable10)